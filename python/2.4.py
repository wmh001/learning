# 数字
# 加
print(3 + 2)
# 减
print(3 - 2)
# 乘
print(2 * 3)
# 除
print(3 / 2)
# 乘方
print(10**6)
print(3**3)
# 优先级
print(2 + 3 * 4)
print((2 + 3) * 4)
# 浮点数
# 通常无需考虑
print(0.1 + 0.1)
print(2 * 0.1)
print(0.2 + 0.2)
print(0.2 * 2)
# 结果包含的小数位数可能不确定
print(0.2 + 0.1)
# str()数字类型转换为字符串
print("Happy "+str(23)+"rd Birthday!")
# python2中两整数相除结果取整数部分
# 当除数或被除数含浮点数是结果为浮点数
